def _gen_body_box(feat: FeatureNode, part: PartNode, var: str) -> str:
    p = feat.params
    d = part.dims
    L = p.get("L", d.get("L", 20))
    W = p.get("W", d.get("W", 15))
    H = p.get("H", d.get("H", 10))
    centered = p.get("centered", True)
    if centered:
        return f'{var} = cq.Workplane("XY").box({L}, {W}, {H})'
    else:
        return f'{var} = cq.Workplane("XY").rect({L}, {W}).extrude({H})'


def _gen_body_cylinder(feat: FeatureNode, part: PartNode, var: str) -> str:
    p = feat.params
    d = part.dims
    R = p.get("R", d.get("R", 10))
    H = p.get("H", d.get("H", 20))
    return f'{var} = cq.Workplane("XY").circle({R}).extrude({H})'


def _gen_body_sphere(feat: FeatureNode, part: PartNode, var: str) -> str:
    p = feat.params
    R = p.get("R", part.dims.get("R", 10))
    return f'{var} = cq.Workplane("XY").sphere({R})'


def _gen_body_cone(feat: FeatureNode, part: PartNode, var: str) -> str:
    p = feat.params
    R1 = p.get("R1", 15)
    R2 = p.get("R2", 5)
    H = p.get("H", 20)
    # CadQuery: loft between two circles
    return (
        f'{var} = (\n'
        f'    cq.Workplane("XY")\n'
        f'    .circle({R1})\n'
        f'    .workplane(offset={H})\n'
        f'    .circle({R2})\n'
        f'    .loft()\n'
        f')'
    )


def _gen_body_torus(feat: FeatureNode, part: PartNode, var: str) -> str:
    p = feat.params
    R1 = p.get("R1", 15)  # major radius
    R2 = p.get("R2", 5)   # minor radius
    return f'{var} = cq.Workplane("XZ").circle({R2}).revolve(360, ({R1}, 0), (0, 1))'


def _gen_body_prism(feat: FeatureNode, part: PartNode, var: str) -> str:
    p = feat.params
    n = p.get("n", 6)
    R = p.get("R", 10)
    H = p.get("H", 20)
    return f'{var} = cq.Workplane("XY").polygon({n}, {R*2}).extrude({H})'


def _gen_pad(feat: FeatureNode, part: PartNode, var: str) -> str:
    p = feat.params
    H = p.get("height", p.get("H", 10))
    sketch = feat.sketch

    if sketch and sketch.get("type") == "rect":
        w, h = sketch.get("W", 20), sketch.get("H", 10)
        return f'{var} = {var}.faces(">Z").workplane().rect({w}, {h}).extrude({H})'
    elif sketch and sketch.get("type") == "circle":
        r = sketch.get("R", 5)
        return f'{var} = {var}.faces(">Z").workplane().circle({r}).extrude({H})'
    elif sketch and sketch.get("type") == "polygon":
        pts = sketch.get("points", [])
        pts_str = ", ".join(f"({x}, {y})" for x, y in pts)
        return (
            f'{var} = (\n'
            f'    {var}.faces(">Z").workplane()\n'
            f'    .polyline([{pts_str}]).close()\n'
            f'    .extrude({H})\n'
            f')'
        )
    else:
        sketch_desc = p.get("sketch_desc", "profile")
        return f'# TODO: Define sketch for pad ({sketch_desc})\n{var} = {var}.faces(">Z").workplane().rect(10, 10).extrude({H})'


def _gen_pocket(feat: FeatureNode, part: PartNode, var: str) -> str:
    p = feat.params
    depth = p.get("depth", 5)
    width = p.get("width", p.get("W", 10))
    height = p.get("height", p.get("H", 10))
    face = p.get("face", ">Z")

    if p.get("shape") == "circle":
        r = p.get("R", width / 2)
        return f'{var} = {var}.faces("{face}").workplane().hole({r*2}, depth={depth})'
    else:
        return f'{var} = {var}.faces("{face}").workplane().rect({width}, {height}).cutBlind(-{depth})'


def _gen_hole(feat: FeatureNode, part: PartNode, var: str) -> str:
    p = feat.params
    d = p.get("diameter", p.get("d", 3.4))
    depth = p.get("depth", "thru")
    face = p.get("face", ">Z")
    pos = p.get("position")

    # 智能面选择: 如果零件有shell且shell移除了目标面，切换到对面
    shell_feats = [f for f in part.features if f.type == FeatureType.SHELL]
    if shell_feats and face == ">Z":
        shell_face = shell_feats[0].params.get("faces_to_remove", ">Z")
        if shell_face == ">Z" and feat.order > shell_feats[0].order:
            face = "<Z"  # shell已移除顶面，改用底面

    lines = []
    wp = f'{var}.faces("{face}").workplane()'
    if pos:
        wp = f'{wp}.center({pos[0]}, {pos[1]})'

    if depth == "thru":
        lines.append(f'{var} = {wp}.hole({d})')
    else:
        lines.append(f'{var} = {wp}.hole({d}, depth={depth})')

    # 沉头孔
    if p.get("countersink"):
        cs_d = p.get("countersink_d", d * 2)
        cs_angle = p.get("countersink_angle", 82)
        lines[-1] = f'{var} = {wp}.cskHole({d}, {cs_d}, {cs_angle})'
    elif p.get("counterbore"):
        cb_d = p.get("counterbore_d", d * 1.8)
        cb_depth = p.get("counterbore_depth", d * 0.5)
        lines[-1] = f'{var} = {wp}.cboreHole({d}, {cb_d}, {cb_depth})'

    return "\n".join(lines)


def _gen_fillet(feat: FeatureNode, part: PartNode, var: str) -> str:
    p = feat.params
    r = p.get("radius", 2)
    edges = p.get("edges", "all")

    if edges == "all":
        return f'{var} = {var}.edges().fillet({r})'
    elif edges == "vertical":
        return f'{var} = {var}.edges("|Z").fillet({r})'
    elif edges == "top":
        return f'{var} = {var}.edges(">Z").fillet({r})'
    elif edges == "bottom":
        return f'{var} = {var}.edges("<Z").fillet({r})'
    else:
        return f'{var} = {var}.edges().fillet({r})  # edges selector: {edges}'


def _gen_chamfer(feat: FeatureNode, part: PartNode, var: str) -> str:
    p = feat.params
    s = p.get("size", 1)
    edges = p.get("edges", "all")

    if edges == "all":
        return f'{var} = {var}.edges().chamfer({s})'
    elif edges == "vertical":
        return f'{var} = {var}.edges("|Z").chamfer({s})'
    else:
        return f'{var} = {var}.edges().chamfer({s})'


def _gen_shell(feat: FeatureNode, part: PartNode, var: str) -> str:
    p = feat.params
    t = p.get("thickness", 2)
    face = p.get("faces_to_remove", ">Z")
    return (
        f'# Shell: remove face {face}, wall={t}mm\n'
        f'{var} = {var}.faces("{face}").shell(-{t})'
    )


def _gen_pattern_linear(feat: FeatureNode, part: PartNode, var: str) -> str:
    p = feat.params
    count = p.get("count", 3)
    spacing = p.get("spacing", 10)
    dx = p.get("dx", spacing)
    dy = p.get("dy", 0)

    # 如果包含孔的位置列表
    positions = p.get("positions")
    hole_d = p.get("hole_d", 3.4)

    if positions:
        lines = []
        for i, pos in enumerate(positions):
            lines.append(f'{var} = {var}.faces(">Z").workplane().center({pos[0]}, {pos[1]}).hole({hole_d})')
            if i < len(positions) - 1:
                lines.append(f'  # hole at ({pos[0]}, {pos[1]})')
        return "\n".join(lines)
    else:
        return (
            f'# Linear pattern: {count}x, spacing ({dx}, {dy})\n'
            f'for _i in range({count}):\n'
            f'    {var} = {var}.faces(">Z").workplane().center({dx}*_i, {dy}*_i).hole({hole_d})'
        )


def _gen_pattern_polar(feat: FeatureNode, part: PartNode, var: str) -> str:
    p = feat.params
    count = p.get("count", 6)
    hole_d = p.get("hole_d", 6.6)
    pcd = p.get("pcd", 45)

    return (
        f'# Polar pattern: {count}x holes on PCD={pcd}mm\n'
        f'_angles = [i * 360 / {count} for i in range({count})]\n'
        f'for _a in _angles:\n'
        f'    _x = {pcd/2} * math.cos(math.radians(_a))\n'
        f'    _y = {pcd/2} * math.sin(math.radians(_a))\n'
        f'    {var} = {var}.faces(">Z").workplane().center(_x, _y).hole({hole_d})'
    )


