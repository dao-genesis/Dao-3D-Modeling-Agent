@dataclass
class CodeResult:
    """代码生成结果"""
    code: str
    engine: str
    parts: List[str]
    output_files: Dict[str, str] = field(default_factory=dict)
    warnings: List[str] = field(default_factory=list)


class ParametricCodegen:
    """
    参数化代码生成器 — 将DesignTree编译为可执行的Python代码。

    道法自然: 生成的代码是自然的、可读的、可修改的。
    唯变所适: 代码可以被LLM理解和迭代修改。
    """

    def __init__(self, engine: str = "cadquery"):
        self.engine = engine

    def generate(self, tree: DesignTree, plan: Optional[BuildPlan] = None,
                 output_dir: str = "./output") -> CodeResult:
        """
        从DesignTree生成完整的参数化建模代码。

        Args:
            tree: 设计树
            plan: 构建计划 (可选，用于确定引擎)
            output_dir: 输出目录路径

        Returns:
            CodeResult 包含生成的代码
        """
        engine = plan.engine if plan else self.engine

        if engine == "cadquery":
            return self._gen_cadquery(tree, output_dir)
        elif engine == "freecad":
            return self._gen_freecad(tree, output_dir)
        else:
            return self._gen_cadquery(tree, output_dir)

    def _gen_cadquery(self, tree: DesignTree, output_dir: str) -> CodeResult:
        """生成 CadQuery 代码"""
        warnings = []
        sections = []

        # 1. 参数块
        params_lines = []
        for k, v in tree.global_params.items():
            if isinstance(v, str):
                params_lines.append(f'{k} = "{v}"')
            else:
                params_lines.append(f'{k} = {v}')

        for part in tree.parts:
            params_lines.append(f'# {part.name} dims')
            for k, v in part.dims.items():
                params_lines.append(f'{part.name}_{k} = {v}')

        params_block = "\n".join(params_lines) if params_lines else "# No global parameters"

        header = CQ_HEADER.format(
            description=tree.description or tree.name,
            name=tree.name,
            params_block=params_block,
        )
        sections.append(header)

        # 2. 零件代码
        part_vars = {}
        for part in tree.parts:
            code_lines = []
            var = f'part_{part.name}'
            part_vars[part.name] = var

            # 按 order 排序特征
            sorted_features = sorted(part.features, key=lambda f: f.order)

            is_first = True
            for feat in sorted_features:
                gen_fn = FEATURE_GENERATORS.get(feat.type)
                if gen_fn is None:
                    warnings.append(f"No generator for feature type: {feat.type.value}")
                    code_lines.append(f'# UNSUPPORTED: {feat.type.value} — {json.dumps(feat.params, ensure_ascii=False)}')
                    continue

                if is_first and feat.type.value.startswith("body_"):
                    # 基体特征 — 创建新变量
                    code_lines.append(gen_fn(feat, part, var))
                    is_first = False
                elif is_first:
                    # 第一个特征不是基体 → 先创建默认基体
                    d = part.dims
                    if d.get("L") and d.get("W") and d.get("H"):
                        code_lines.append(f'{var} = cq.Workplane("XY").box({d["L"]}, {d["W"]}, {d["H"]})')
                    elif d.get("R") and d.get("H"):
                        code_lines.append(f'{var} = cq.Workplane("XY").circle({d["R"]}).extrude({d["H"]})')
                    else:
                        code_lines.append(f'{var} = cq.Workplane("XY").box(20, 15, 10)  # default body')
                    is_first = False
                    code_lines.append(gen_fn(feat, part, var))
                else:
                    # 后续特征 — 追加到现有变量
                    code_lines.append(gen_fn(feat, part, var))

            # 如果零件没有特征但有dims，自动创建基体
            if not part.features:
                d = part.dims
                if d.get("L") and d.get("W") and d.get("H"):
                    code_lines.append(f'{var} = cq.Workplane("XY").box({d["L"]}, {d["W"]}, {d["H"]})')
                elif d.get("R") and d.get("H"):
                    code_lines.append(f'{var} = cq.Workplane("XY").circle({d["R"]}).extrude({d["H"]})')
                else:
                    code_lines.append(f'{var} = cq.Workplane("XY").box(20, 15, 10)  # placeholder')
                    warnings.append(f"Part '{part.name}' has no features or dims — using placeholder")

            section = CQ_PART_TEMPLATE.format(
                name=part.name,
                function=part.function.value,
                code="\n".join(code_lines),
            )
            sections.append(section)

        # 3. 装配 / 连接
        if tree.joins:
            assy_lines = []
            for join in tree.joins:
                va = part_vars.get(join.part_a, f'part_{join.part_a}')
                vb = part_vars.get(join.part_b, f'part_{join.part_b}')

                if join.method == JoinMethod.FUSE:
                    pos = join.params.get("position")
                    if pos:
                        assy_lines.append(
                            f'# Fuse {join.part_a} + {join.part_b}\n'
                            f'{vb}_placed = {vb}.translate(({pos[0]}, {pos[1]}, {pos[2]}))\n'
                            f'result = {va}.union({vb}_placed)'
                        )
                    else:
                        assy_lines.append(f'result = {va}.union({vb})')
                elif join.method == JoinMethod.NONE:
                    pass
                else:
                    assy_lines.append(
                        f'assy.add({va}, name="{join.part_a}")\n'
                        f'assy.add({vb}, name="{join.part_b}")'
                    )

            if assy_lines:
                has_fuse = any(j.method == JoinMethod.FUSE for j in tree.joins)
                has_assy = any(j.method not in (JoinMethod.FUSE, JoinMethod.NONE) for j in tree.joins)
                preamble = ""
                if has_assy:
                    preamble = "assy = cq.Assembly()\n"
                sections.append("\n# ═══ Assembly / Joins ═══\n" + preamble + "\n\n".join(assy_lines))
                # Set result from fuse chain or first part
                if not has_fuse:
                    first_var = list(part_vars.values())[0]
                    sections.append(f'\nresult = {first_var}  # individual parts in assy')

        # 如果没有连接 或 连接中没有fuse产生result，设置result = 第一个零件
        has_result = any('result =' in s for s in sections)
        if not has_result and part_vars:
            first_var = list(part_vars.values())[0]
            sections.append(f'\nresult = {first_var}')

        # 4. 导出
        export_lines = []
        out = output_dir.replace("\\", "/")
        name = tree.name
        output_files = {}

        export_lines.append(f'os.makedirs(r"{out}", exist_ok=True)')

        for fmt in ["stl", "step"]:
            fpath = f'{out}/{name}.{fmt}'
            output_files[fmt] = fpath
            if fmt == "stl":
                export_lines.append(f'exporters.export(result, r"{fpath}")')
            elif fmt == "step":
                export_lines.append(f'exporters.export(result, r"{fpath}", exportType="STEP")')

        export_section = CQ_EXPORT.format(
            export_code="\n".join(export_lines),
            name=name,
        )
        sections.append(export_section)

        code = "\n".join(sections)

        return CodeResult(
            code=code,
            engine="cadquery",
            parts=list(part_vars.keys()),
            output_files=output_files,
            warnings=warnings,
        )

    def _gen_freecad(self, tree: DesignTree, output_dir: str) -> CodeResult:
        """生成 FreeCAD PartDesign 代码"""
        warnings = []
        lines = [
            '#!/usr/bin/env python3',
            '"""',
            f'{tree.description or tree.name}',
            'Generated by 道·参数化代码生成器 v1.0 (FreeCAD PartDesign)',
            '"""',
            'import FreeCAD as App',
            'import Part',
            'from FreeCAD import Base',
            'import os',
            '',
            f'OUTPUT_DIR = r"{output_dir}"',
            'os.makedirs(OUTPUT_DIR, exist_ok=True)',
            '',
            f'doc = App.newDocument("{tree.name}")',
            '',
        ]

        part_vars = {}
        for part in tree.parts:
            var = f'obj_{part.name}'
            part_vars[part.name] = var

            lines.append(f'# ─── Part: {part.name} ───')
            d = part.dims
            if d.get("L") and d.get("W") and d.get("H"):
                lines.append(f'{var} = doc.addObject("Part::Feature", "{part.name}")')
                lines.append(f'{var}.Shape = Part.makeBox({d["L"]}, {d["W"]}, {d["H"]})')
            elif d.get("R") and d.get("H"):
                lines.append(f'{var} = doc.addObject("Part::Feature", "{part.name}")')
                lines.append(f'{var}.Shape = Part.makeCylinder({d["R"]}, {d["H"]})')
            else:
                lines.append(f'{var} = doc.addObject("Part::Feature", "{part.name}")')
                lines.append(f'{var}.Shape = Part.makeBox(20, 15, 10)')

            # Apply features
            for feat in sorted(part.features, key=lambda f: f.order):
                if feat.type == FeatureType.FILLET:
                    r = feat.params.get("radius", 2)
                    lines.append(f'try:')
                    lines.append(f'    {var}.Shape = {var}.Shape.makeFillet({r}, {var}.Shape.Edges)')
                    lines.append(f'except: pass  # fillet may fail on some edges')
                elif feat.type == FeatureType.SHELL:
                    t = feat.params.get("thickness", 2)
                    lines.append(f'try:')
                    lines.append(f'    _faces = [{var}.Shape.Faces[0]]  # top face')
                    lines.append(f'    {var}.Shape = {var}.Shape.makeThickness(_faces, -{t}, 0.001)')
                    lines.append(f'except: pass')

            lines.append('')

        # Export
        lines.append('doc.recompute()')
        name = tree.name
        output_files = {}

        stl_path = f'{output_dir}/{name}.stl'.replace("\\", "/")
        step_path = f'{output_dir}/{name}.step'.replace("\\", "/")
        fcstd_path = f'{output_dir}/{name}.FCStd'.replace("\\", "/")

        output_files["stl"] = stl_path
        output_files["step"] = step_path
        output_files["fcstd"] = fcstd_path

        lines.append(f'# Export')
        lines.append(f'import Mesh')

        first_var = list(part_vars.values())[0] if part_vars else "None"
        lines.append(f'if {first_var} and hasattr({first_var}, "Shape"):')
        lines.append(f'    Mesh.export([{first_var}], r"{stl_path}")')
        lines.append(f'    Part.export([{first_var}.Shape], r"{step_path}")')
        lines.append(f'doc.saveAs(r"{fcstd_path}")')
        lines.append(f'print("Build complete: {name}")')

        code = "\n".join(lines)
        return CodeResult(
            code=code, engine="freecad",
            parts=list(part_vars.keys()),
            output_files=output_files,
            warnings=warnings,
        )

    def execute(self, code_result: CodeResult, output_dir: Optional[str] = None) -> Dict:
        """
        执行生成的代码并返回结果。

        这是与 DaoEngine 的桥接点 — 实际执行委托给 DaoEngine。
        """
        # 保存代码到文件
        if output_dir:
            out = Path(output_dir)
        else:
            out = Path(code_result.output_files.get("stl", "./output")).parent

        out.mkdir(parents=True, exist_ok=True)
        script_path = out / f"{code_result.engine}_build.py"
        script_path.write_text(code_result.code, encoding="utf-8")

        return {
            "ok": True,
            "script_path": str(script_path),
            "engine": code_result.engine,
            "parts": code_result.parts,
            "output_files": code_result.output_files,
            "warnings": code_result.warnings,
            "note": "Code saved. Execute with DaoEngine or directly with CadQuery/FreeCAD.",
        }


