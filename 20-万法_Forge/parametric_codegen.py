def _gen_custom_sketch(feat: FeatureNode, part: PartNode, var: str) -> str:
    p = feat.params
    sketch = feat.sketch
    H = p.get("height", p.get("H", 10))

    if not sketch:
        return f'# Custom sketch: define points\n{var} = cq.Workplane("XY").rect(20, 10).extrude({H})'

    sketch_type = sketch.get("type", "polyline")
    if sketch_type == "polyline":
        pts = sketch.get("points", [(0,0), (20,0), (20,10), (0,10)])
        pts_str = ", ".join(f"({x}, {y})" for x, y in pts)
        return (
            f'{var} = (\n'
            f'    cq.Workplane("XY")\n'
            f'    .polyline([{pts_str}]).close()\n'
            f'    .extrude({H})\n'
            f')'
        )
    elif sketch_type == "circle":
        r = sketch.get("R", 10)
        return f'{var} = cq.Workplane("XY").circle({r}).extrude({H})'
    else:
        return f'# Custom sketch type: {sketch_type}\n{var} = cq.Workplane("XY").rect(20, 10).extrude({H})'


